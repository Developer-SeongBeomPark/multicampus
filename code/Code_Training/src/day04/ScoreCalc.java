package day04;

class Score{
	private String name;
	private int kuk, eng, math;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getKuk() {
		return kuk;
	}
	public void setKuk(int kuk) {
		this.kuk = kuk;
	}
	public int getEng() {
		return eng;
	}
	public void setEng(int eng) {
		this.eng = eng;
	}
	public int getMath() {
		return math;
	}
	public void setMath(int math) {
		this.math = math;
	}
	
}

public class ScoreCalc{
	public static void main(String[] args) {
		Score score = new Score();
		score.setKuk(40);
		System.out.println(score.getKuk());
	}
}